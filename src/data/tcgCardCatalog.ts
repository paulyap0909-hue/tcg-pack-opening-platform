export const getTcgCardStats = () => {
  const pokemonCount = tcgCardCatalog.filter((card) => card.game === 'pokemon').length
  const onePieceCount = tcgCardCatalog.filter((card) => card.game === 'one_piece').length
  const setCount = new Set(tcgCardCatalog.map((card) => `${card.game}-${card.setCode}`)).size
  const rarityCount = new Set(tcgCardCatalog.map((card) => card.rarity)).size

  return {
    total: tcgCardCatalog.length,
    pokemonCount,
    onePieceCount,
    setCount,
    rarityCount,
  }
}
