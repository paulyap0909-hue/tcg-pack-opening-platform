export type Translation = typeof translations.en

export const getLanguageLabel = (language: AppLanguage) =>
  languageOptions.find((option) => option.code === language)?.nativeLabel ?? 'English'

export const normalizeLanguage = (value: string | null): AppLanguage => {
  if (value === 'zh' || value === 'ms' || value === 'en') return value
  return 'en'
}

export const loadStoredLanguage = (): AppLanguage => {
  if (typeof window === 'undefined') return 'en'
  return normalizeLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY))
}

export const saveStoredLanguage = (language: AppLanguage) => {
  if (typeof window === 'undefined') return
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language)
}
